export interface Enchantment {
  /** Chave estável (kebab-case, baseada no nome em inglês). */
  slug: string;
  /** Nome em português exibido em destaque. */
  name: string;
  /** Nome original em inglês (é o que aparece no jogo). */
  original: string;
  category: string;
  /** Nível máximo do encanto. */
  maxLevel: number;
  /** Em que itens o encanto pode ser aplicado (rótulo pronto). */
  items: string;
  /** Onde se consegue. */
  sources: EnchantSource[];
  /** O que o encanto faz, em português direto. */
  desc: string;
  /** Pra que serve na prática. */
  serve: string;
}

